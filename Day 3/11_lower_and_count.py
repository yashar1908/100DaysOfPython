#lower() function is the same as .toLowerCase()
#It takes a string and converts it into lower case
#syntax: "String".lower()
print("Yash".lower())
print("YASH".lower())


#count() function takes a character arguement and counts the occurences-
#of that character in the given string.
# Syntax: "String".count(character)

print(("Yash".lower()).count("y")) #converts Yash to yash then looks for y

## count() is case sensitive. Y dhundhne bola toh y ko count nahi karega
# "Yash".count("y") is zero.

